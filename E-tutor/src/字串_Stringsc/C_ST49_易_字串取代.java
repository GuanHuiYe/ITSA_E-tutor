package 字串_Stringsc;

import java.util.*;

public class C_ST49_易_字串取代 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.println(sc.nextLine().replace("hate", "love")
				.replace("stupid", "smart")
				.replace("angry", "happy")
				.replace("dirty", "clean"));

	}

}
